marks=int(input("Enter the marks:"))
if marks<40:
    print("Grade is F.")
elif marks>=40 and marks<50:
    print("Grade is D.")
elif marks>=50 and marks<60:
    print("Grade is C.")
elif marks>=60 and marks<70:
    print("Grade is B.")
elif marks>=70 and marks<80:
    print("Grade is A.")
elif marks>=80 and marks<90:
    print("Grade is E.")
elif marks>=90:
    print("Grade is O")
